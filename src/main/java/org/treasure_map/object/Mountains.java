package org.treasure_map.object;

public class Mountains {

    private final int xAxis;
    private final int yAxis;

    public Mountains(int xAxis, int yAxis) {
        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public int getXAxis() {
        return xAxis;
    }

    public int getYAxis() {
        return yAxis;
    }
}
